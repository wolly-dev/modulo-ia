# Este programa calcula dois números inseridos pelo user

# Solicitando o usuário os números (inteiros!) a serem somados:
n1 = int(input("Digite um número inteiro: "))
n2 = int(input("Digite outro número inteiro: "))

# Exibindo o resultado dos números, e a soma deles:
print(f"Os números {n1} e {n2} somados dão o resultado de {n1+n2}!")